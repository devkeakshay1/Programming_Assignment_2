
### 1. Write a Python program to convert kilometers to miles?
##### Sol.

km = float(input("The value in Km is :"))

#The formula for conversion of km into miles is miles=km/1.60934

miles = km/1.60934

print("For {}km equivalent distance in miles = {}".format(km, miles))


